<p>Postmatic Social Commenting takes a lighter weight approach than traditional Social Login plugins. </p>

<h1>The big difference</h1>

<p>No WordPress users are created. Nobody is logged in. This isn't <em>social login</em>, it's <em>social commenting</em>. That means it's simple, lean, and <strong>extremely fast</strong>.</p>

<h1>How it works</h1>

<ol>
<li>Users click the social network icon of their choice when they want to leave a comment.</li>
<li>The social network asks their permission to share their name, profile image, and email address with your site.</li>
<li>If they agree A standard WordPress cookie is added to their browser and the comment form is ready for their reply. When they return to your site they are already authorized.</li>
</ol>

<h1>Get started</h1>

<p>On the tabs above you can choose which networks you would like to add to your comment form. Configuration with each social network can be <em>oh so no fun</em>. That's why we've provided some videos to walk you through it. Just think about all the time you'll save your users and all the nice new comments you'll receive.</p>

